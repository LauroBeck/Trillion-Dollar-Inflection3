-- DESCRIPTION: JPM BI Intelligence with sanitized S&P variables for Oracle SQL*Plus.

SET DEFINE OFF;
SET PAGESIZE 50;
SET LINESIZE 150;

COLUMN METRIC FORMAT A20;
COLUMN DELTA FORMAT A15;
COLUMN STATUS FORMAT A15;

-- 1. JPM FINANCIAL PERFORMANCE (THE $50B RECORD)
SELECT 'JPM_TOTAL_REVENUE' AS METRIC, 50.50 AS VAL_BILLIONS, '10%_YOY' AS DELTA, 'RECORD' AS STATUS FROM DUAL
UNION ALL
SELECT 'JPM_NET_INCOME', 16.50, '27%_QOQ', 'DOMINANT' FROM DUAL
UNION ALL
SELECT 'IB_WALLET_SHARE', 9.80, '#1_RANK', 'ALPHA_LEADER' FROM DUAL;

-- 2. MARKET TARGETS & LIQUIDITY (7,200 SP500 SYNC)
SELECT 'SP500_2026_TARGET' AS METRIC, 7200.00 AS VAL_OR_PTS, 'REVISED' AS DELTA, 'JPM_OUTLOOK' AS STATUS FROM DUAL
UNION ALL
SELECT 'FORTRESS_CAPITAL', 291.00, 'CET1', 'STABLE' FROM DUAL
UNION ALL
SELECT 'AUM_LIQUIDITY', 4.80, 'TRILLIONS', '16%_YOY_UP' FROM DUAL;

COMMIT;
